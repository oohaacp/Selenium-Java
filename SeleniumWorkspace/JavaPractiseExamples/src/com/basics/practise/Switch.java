package com.basics.practise;

public class Switch {

	public static void main(String[] args) {
		String weekday ="Tuesday";
		/*
		 * switch(weekday) 
		 * { 
		 * case 1: 
		 * System.out.println("monday");
		 * 
		 * 
		 * break;
		 * 
		 * 
		 * case 2:
		 * 
		 * System.out.println("Tuesday"); break; case 3:
		 * System.out.println("thursaday"); break;
		 * 
		 * }
		 */
		switch(weekday)
		{
		case "Monday":
		
		System.out.println("monday");
		break;
		
		case "Tuesday":
		
			System.out.println("Tuesaday");
			break;
		
		case "Thursday":
		
			System.out.println("Thursday");
		
		}

	}
}
