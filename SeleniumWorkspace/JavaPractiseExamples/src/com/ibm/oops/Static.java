package com.ibm.oops;

public  class  Static 
{
	public static String name ="oohaa";
	public static int i=4000;

	public static void main(String[] args)
	{
	Static.pop();
	Static.pulp();

	}
	public static void pop()
	{
		System.out.println(name);
	}
	public static void pulp()
	{
		System.out.println(i);
	}

}
